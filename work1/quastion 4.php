<?php
$dayNumber = 3; 


if ($dayNumber == 1) {
    echo "Monday";
} elseif ($dayNumber == 2) {
    echo "Tuesday";
} elseif ($dayNumber == 3) {
    echo "Wednesday";
} elseif ($dayNumber == 4) {
    echo "Thursday";
} elseif ($dayNumber == 5) {
    echo "Friday";
} elseif ($dayNumber == 6) {
    echo "Saturday";
} elseif ($dayNumber == 7) {
    echo "Sunday";
} else {
    echo "Invalid input. Please enter a number between 1 and 7.";
}

?>
